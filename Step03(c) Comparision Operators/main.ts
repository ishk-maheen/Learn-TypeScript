// comparision operator
let a = 5;
let b = 10;

//greater than
console.log(a > b);

//less than
console.log(a < b);

//greater than or equal
console.log(a >= b);

//less than or equal
console.log(a <= b);



let c:any = 20;
let d:any = "20";

//equal to
console.log(c == d);

//not equal to
console.log(a != b);

//strict equal to 
console.log(a === b);

//strict not equal to 
console.log(a !== b);


